int n, q;
 
struct node {
	ll s, ml, mr, mm, mx;
} st[1<<20];
 
void upd(int l1, ll x, int i=1, int l2=0, int r2=n-1) {
	if(l2==r2) {
		st[i].s=x;
		st[i].mm=x;
		st[i].ml=x;
		st[i].mr=x;
		st[i].mx=x;
		return;
	}
	int m2=(l2+r2)/2;
	if(l1<=m2) 
		upd(l1, x, 2*i, l2, m2);
	else  
		upd(l1, x, 2*i+1, m2+1, r2);
	st[i].mx=max({st[2*i].mx, st[2*i+1].mx, st[2*i+1].ml+st[2*i].mr});
	st[i].ml=max(st[2*i].ml,st[2*i].mm+st[2*i+1].ml);
	st[i].mr=max(st[2*i+1].mr, st[2*i].mr+st[2*i+1].mm);
	st[i].mm=st[2*i].mm+st[2*i+1].mm;
}
 
void solve() {
	read(n, q);
	FOR(n) {
		ll op=0;
		read(op);
		upd(i, op);
	}
	while(q--) {
		int p, v;
		read(p, v);
		--p;
		upd(p, v);
		print(max(st[1].mx, 0LL));
	}
}
 
