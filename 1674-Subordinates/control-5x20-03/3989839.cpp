int sub(int x , vector <int> *v , vector <int> &dp){
  if(v[x].size()==0)return dp[x]= 0;
  if(dp[x]!=-1)return dp[x];
 
  int sum=0;
  
  for(int i=0 ; i<v[x].size() ; i++){
    sum+=sub(v[x][i] , v ,dp)+1;
  }
  return dp[x]=sum;
}
 
