const lli N = 600, inf = 1e15;
lli v[N][N];
lli d[N][N];
int main(){
	Magic
	debugMode();
	lli n, m, q;
	cin >> n >> m >> q;
	for (lli i = 1 ; i <= m ; i++){
		lli a, b, c;
		cin >> a >> b >> c;
		if (v[a][b] == 0){
			v[a][b] = c;
			v[b][a] = c;
		}
		else{
			v[a][b] = min(v[a][b], c);
			v[b][a] = v[a][b];
		}
	}
	for (lli i = 1 ; i <= n ; i++){
		for (lli j = 1 ; j <= n ; j++){
			if (v[i][j] > 0)
				d[i][j] = v[i][j];
			else
				d[i][j] = inf;
			if (i == j)
				d[i][j] = 0;
		}
	}
	for (lli i = 1 ; i <= n ; i++){
		for (lli j = 1 ; j <= n ; j++){
			for (lli h = 1 ; h <= n ; h++){
				d[j][h] = min(d[j][h], d[j][i] + d[i][h]);
			}
		}
	}
	for (lli i = 1 ; i <= n ; i++){
		for (lli j = 1 ; j <= n ; j++){
			// cout << d[i][j] << " ";
			if (d[i][j] >= inf){
				d[i][j] = -1;
			}
		}
		// cout << "\n";
	}
	while (q--){
		lli x, y;
		cin >> x >> y;
		cout << d[x][y] << "\n";
	}
}
